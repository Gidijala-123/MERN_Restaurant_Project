/**
 * Helper function to get items by category
 */
export const getItemsByCategory = (category) => {
  return MENU_DATA.filter((item) => item.category === category);
};

/**
 * Helper function to get items by category and subcategory
 */
export const getItemsBySubCategory = (category, subCategory) => {
  return MENU_DATA.filter(
    (item) => item.category === category && item.subCategory === subCategory,
  );
};

/**
 * Helper function to get unique subcategories for a category
 */
export const getSubCategoriesByCategory = (category) => {
  const items = getItemsByCategory(category);
  const subCategories = [...new Set(items.map((item) => item.subCategory))];
  return subCategories;
};

/**
 * Helper function to get all unique categories
 */
export const getAllCategories = () => {
  return [...new Set(MENU_DATA.map((item) => item.category))];
};

