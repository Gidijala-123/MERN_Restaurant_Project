const migrateMenuData = async () => {
  try {
    // Connect to database
    await dbConnection();

    // Check if data already exists
    const count = await MenuItem.countDocuments();
    if (count > 0) {
      logger.info(
        `Menu data already exists. Found ${count} items. Clearing before re-import...`,
      );
      await MenuItem.deleteMany({});
    }

    // Insert menu items
    const result = await MenuItem.insertMany(MENU_DATA);

    logger.info(
      `✅ Successfully migrated ${result.length} menu items to database`,
    );

    // Log statistics
    const categories = await MenuItem.distinct("category");
    logger.info(`📊 Categories: ${categories.length}`);
    logger.info(`📋 Categories: ${categories.join(", ")}`);

    console.log("\n✅ Migration completed successfully!");
    console.log(`Total items inserted: ${result.length}`);
    console.log(`Total categories: ${categories.length}`);

    process.exit(0);
  } catch (error) {
    logger.error("❌ Migration failed:", error);
    console.error("Migration error:", error);
    process.exit(1);
  }
};

// Run migration
migrateMenuData();
